
package MyProject;


public class TransportObject {
    String area;
    int rent;

    public TransportObject() {
    }

    public TransportObject(String area, int rent) {
        this.area = area;
        this.rent = rent;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

    public int getRent() {
        return rent;
    }

    public void setRent(int rent) {
        this.rent = rent;
    }
    
    
}
